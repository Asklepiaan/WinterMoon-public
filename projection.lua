while not Metal.checkClose() do
	wtime = Winter.getClock()
	if (debugger == true) then
		debuglog = 'LOG: ' .. WINTER_PROCESSOR_NAME .. ':' .. WINTER_OS_PLATFORM .. ':' .. WINTER_OS_VERSION .. '\n'
		debugentries = 0
	end

	oldr = WINTER_KEY_R
	oldf = WINTER_KEY_F
	Metal.updateInput()
	oldx = playerx
	oldy = playery
	playerx, playery = Plush.getPlayerPosition()
	if (mlatch == false) then
		accuracy = accuracy - 1
	end
	mlatch = false
	if (WINTER_KEY_W) then
		Plush.movePlayer(1, true)
		accuracy = accuracy + 1; alatch = false; mlatch = true
	end
	if (WINTER_KEY_S) then
		Plush.movePlayer(-1, true)
		accuracy = accuracy + 1; alatch = false; mlatch = true
	end
	if (WINTER_KEY_Q) then
		Plush.movePlayer(-1, false)
		accuracy = accuracy + 1; alatch = false; mlatch = true
	end
	if (WINTER_KEY_E) then
		Plush.movePlayer(1, false)
		accuracy = accuracy + 1; alatch = false; mlatch = true
	end
	if (WINTER_KEY_A) then
		direction = direction - 15
		accuracy = accuracy + 1; alatch = false; mlatch = true
	end
	if (WINTER_KEY_D) then
		direction = direction + 15
		accuracy = accuracy + 1; alatch = false; mlatch = true
	end
	if (WINTER_KEY_UP) then
		angle = angle - 15
		accuracy = accuracy + 1; alatch = false; mlatch = true
	end
	if (WINTER_KEY_DOWN) then
		angle = angle + 15
		accuracy = accuracy + 1; alatch = false; mlatch = true
	end
	if (WINTER_KEY_LEFT) then
		Plush.setRenderDistance(Plush.getRenderDistance() - 1)
		accuracy = accuracy + 1; alatch = false; mlatch = true
	end
	if (WINTER_KEY_RIGHT) then
		Plush.setRenderDistance(Plush.getRenderDistance() + 1)
		accuracy = accuracy + 1; alatch = false; mlatch = true
	end
	if (WINTER_KEY_R) and not (WINTER_KEY_R == oldr) then
		threads = threads + 1
	end
	if (WINTER_KEY_F) and not (WINTER_KEY_F == oldf) then
		threads = threads - 1
	end
	if (threads <= 0) then
		threads = 1
	end
--	if (Plush.checkWall(playerx, playery, testroom.wallsVector)) then
--		playerx = oldx
--		playery = oldy
--		Plush.setPlayerPosition(playerx, playery)
--	end
	Plush.setPlayerRotation(direction * 0.0174533)
	Plush.setTilt(angle * 0.0174533)
	Plush.threads(threads)
	if (accuracy < 1) then
		accuracy = 1
	end
	if (accuracy > 2) then
		accuracy = 2
	end
	if not (Plush.getAccuracy() == Winter.round(accuracy, 0)) then
		alatch = false
	end
	Plush.setAccuracy(Winter.round(accuracy, 0))

	if (alatch == false) then
		drawbuffer = Plush.sortVertex(vertexbuffer, playerx, playery, Plush.getPlayerHeight(), 90, 90, 0)
		focus = 100
		for i = 1, #drawbuffer do
			tlx, tly = Plush.project(drawbuffer[i].topleft.x, drawbuffer[i].topleft.y, drawbuffer[i].topleft.z, focus)
			trx, try = Plush.project(drawbuffer[i].topright.x, drawbuffer[i].topright.y, drawbuffer[i].topright.z, focus)
			blx, bly = Plush.project(drawbuffer[i].bottomleft.x, drawbuffer[i].bottomleft.y, drawbuffer[i].bottomleft.z, focus)
			brx, bry = Plush.project(drawbuffer[i].bottomright.x, drawbuffer[i].bottomright.y, drawbuffer[i].bottomright.z, focus)
			Plush.skew(drawbuffer[i].image, WINTER_FRAMEBUFFER, tlx + 240, tly + 136, trx + 240, try + 136, blx + 240, bly + 136, brx + 240, bry + 136)
		end
		--Plush.raycast(WINTER_FRAMEBUFFER, threads)
		alatch = true
		Plush.dither(WINTER_FRAMEBUFFER, 12.5, false)
	end

	Metal.renderImage(WINTER_FRAMEBUFFER)
	--Plush.printImage(WINTER_FRAMEBUFFER, 80, 24); debugger = false
	if (debugger == true) then
		debugentries = debugentries + 1
		debuglog = debuglog .. debugentries .. ': threads allowed: ' .. threads .. '\n'
	end

	if (WINTER_KEY_RETURN == true) then
		window = Metal.getWindow()
		Metal.saveImage('/Users/plushie/Desktop/test.png', window)
	end

	if (drawFrame) then
		Metal.forceUpdate()
		Metal.clearBuffer(framecolour[1], framecolour[2], framecolour[3])
	end
	wtime = Winter.getClock()
	if (wtime <= WINTER_FRAMERATE) then
		Winter.sleep(math.floor(WINTER_FRAMERATE - wtime + 0.5))
	else
		if (debugger == true) then
			debugentries = debugentries + 1
			debuglog = debuglog .. debugentries .. ': Frame took too long to draw!!\n   Drawtime: ' .. wtime .. 'ms\n'
		end
	end
	if (debugger == true) then
		frames = frames + 1
		debugentries = debugentries + 1
		debuglog = debuglog .. debugentries .. ': Frame #' .. frames .. '\n   render time: ' .. wtime .. 'ms\n   drawtime: ' .. WINTER_FRAMERATE - wtime .. 'ms\n'
		print(debuglog)
	end
end

